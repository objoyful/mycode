# Ok, in this short cource I will go through the following commands:

pwd # Print working directory.
mkdir # Make directory.
cd # Change directory.
ls # List directory.
rmdir # Remove directory.
pushd # Save your location and go to a new one.
popd # Return to the saved location.
New-Item # Make an empty file.
cp # Copy file from one location to another.
mv # Move file from one location to another.
more # View a file.
rm # Remove a file.
exit # Exit the terminal.